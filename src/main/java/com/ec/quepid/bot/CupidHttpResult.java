public class CupidHttpResult {
    
    private ArrayList<CupidItem> items;
    private Integer totalNumberOfProducts ;
    private Integer totalNumberOfCategories;
    private boolean validSearchLogic;
    private boolean validSearchConfig;
    private boolean validEnvironmentVariable; 
    private boolean validLocaleVariable;

    public CupidHttpResult(ArrayList<CupidItem> items, Integer totalNumberOfProducts, Integer totalNumberOfCategories, boolean validSearchLogic, boolean validSearchConfig, boolean validEnvironmentVariable, boolean validLocaleVariable) {
        this.items = items;
        this.totalNumberOfProducts = totalNumberOfProducts;
        this.totalNumberOfCategories = totalNumberOfCategories;
        this.validSearchLogic = validSearchLogic;
        this.validSearchConfig = validSearchConfig;
        this.validEnvironmentVariable = validEnvironmentVariable;
        this.validLocaleVariable = validLocaleVariable;
    }
	/**
     * @return the totalNumberOfCategories
     */
    public Integer getTotalNumberOfCategories() {
        return totalNumberOfCategories;
    }
    /**
     * @param totalNumberOfCategories the totalNumberOfCategories to set
     */
    public void setTotalNumberOfCategories(Integer totalNumberOfCategories) {
        this.totalNumberOfCategories = totalNumberOfCategories;
    }
    
	/**
     * @return the items
     */
    public ArrayList<CupidItem> getItems() {
        return items;
    }
    
    /**
     * @param items the items to set
     */
    public void setItems(ArrayList<CupidItem> items) {
        this.items = items;
    }
    /**
     * @return the totalNumberOfProducts
     */
    public Integer getTotalNumberOfProducts() {
        return totalNumberOfProducts;
    }
    /**
     * @param totalNumberOfProducts the totalNumberOfProducts to set
     */
    
    public void setTotalNumberOfProducts(Integer totalNumberOfProducts) {
        this.totalNumberOfProducts = totalNumberOfProducts;
    }
    
    public boolean isValidSearchLogic() {
		return validSearchLogic;
	}
    
	public void setValidSearchLogic(boolean validSearchLogic) {
		this.validSearchLogic = validSearchLogic;
	}
	
	public boolean isValidSearchConfig() {
		return validSearchConfig;
	}
	
	public void setValidSearchConfig(boolean validSearchConfig) {
		this.validSearchConfig = validSearchConfig;
	}
	
    public boolean isValidEnvironmentVariable() {
		return validEnvironmentVariable;
	}
    
	public void setValidEnvironmentVariable(boolean validEnvironmentVariable) {
		this.validEnvironmentVariable = validEnvironmentVariable;
	}
	
	public boolean isValidLocaleVariable() {
		return validLocaleVariable;
	}
	
	public void setValidLocaleVariable(boolean validLocaleVariable) {
		this.validLocaleVariable = validLocaleVariable;
	}
    

}
