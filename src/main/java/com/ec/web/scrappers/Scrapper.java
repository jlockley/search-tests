public abstract class Scrapper{
    
    private WebDriverPageOps pageOps;
    
    public Scrapper(WebDriverPageOps webDriverPageOps){
        this.pageOps = webDriverPageOps;
    }
    
    public abstract void run();
     
   
    public WebDriverPageOps getPageOps(){
        return pageOps;
    }
}
